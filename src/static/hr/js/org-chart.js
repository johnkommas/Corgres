  function toFlat(branchFilter = 'all', deptFilter = 'all') {
    const items = [];
    let counter = 0;

    const branches = data.children || [];

    function walk(node, parentId, currentBranch, path) {
      const id = `${path}/${node.name}-${counter++}`;
      const roleBranch = node.type === 'branch' ? 'Κατάστημα' : (currentBranch || '');
      items.push({ id, parentId, name: node.name, role: node.name, branch: roleBranch });
      const nextBranch = node.type === 'branch' ? node.name : currentBranch;
      if (node.children && node.children.length) {
        node.children.forEach((child) => walk(child, id, nextBranch, id));
      }
    }

    if (branchFilter === 'all') {
      // Include the actual root from data and branches underneath
      const rootId = `root-${data.name}`;
      items.push({ id: rootId, parentId: null, name: data.name, role: data.name, branch: '' });
      branches.forEach(b => walk(b, rootId, undefined, rootId));
    } else {
      // For a specific branch (e.g., Ρέθυμνο/Χανιά), allow optional department filter
      const branchNode = branches.find(b => b.name === branchFilter);
      if (branchNode && Array.isArray(branchNode.children)) {
        if (branchFilter === 'Ρέθυμνο' && deptFilter && deptFilter !== 'all') {
          const deptNode = branchNode.children.find(c => c.name === deptFilter);
          if (deptNode) {
            const id = `${branchFilter}/${deptNode.name}-${counter++}`;
            // Selected department as root for this view
            items.push({ id, parentId: null, name: deptNode.name, role: deptNode.name, branch: '' });
            if (deptNode.children && deptNode.children.length) {
              deptNode.children.forEach(grand => walk(grand, id, '', id));
            }
          }
        } else {
          // No department filter: show all first-level children under the branch
          branchNode.children.forEach(child => {
            const id = `${branchFilter}/${child.name}-${counter++}`;
            // Top-level under selected branch should have no parent to remove root/branch levels
            items.push({ id, parentId: null, name: child.name, role: child.name, branch: '' });
            // Continue walking children, with empty branch label to avoid showing the branch subtitle
            if (child.children && child.children.length) {
              child.children.forEach(grand => walk(grand, id, '', id));
            }
          });
        }
      }
    }
    return items;
  }

  // Fit Google OrgChart to container without scrollbars by scaling
  let innerDiv = null;
  let resizeTimer = null;
  function fitChartToContainer() {
    if (!innerDiv || !orgContainer) return;
    // reset any previous transform to measure natural size
    innerDiv.style.transform = 'none';
    innerDiv.style.width = 'auto';
    innerDiv.style.height = 'auto';

    const containerW = orgContainer.clientWidth - 8; // account for borders/padding
    const containerH = orgContainer.clientHeight - 8;
    const contentW = innerDiv.scrollWidth;
    const contentH = innerDiv.scrollHeight;
    if (contentW === 0 || contentH === 0) return;
    const scale = Math.min(containerW / contentW, containerH / contentH, 1);

    innerDiv.style.transformOrigin = 'top center';
    innerDiv.style.transform = `scale(${scale})`;
    innerDiv.style.width = contentW + 'px';
    innerDiv.style.height = contentH + 'px';
  }

  function scheduleFit() {
    if (resizeTimer) clearTimeout(resizeTimer);
    resizeTimer = setTimeout(() => fitChartToContainer(), 80);
  }

